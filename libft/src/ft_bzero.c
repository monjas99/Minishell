#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*dst;

	i = 0;
	dst = (char *)s;
	while (i < n)
	{
		dst[i] = 0;
		i++;
	}
	return ;
}
